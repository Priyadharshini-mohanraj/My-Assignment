package day2assignment;

public class CalculatorClass {
	public int add(int a, int b, int c) {
		return a + b + c;
	}
	public int sub (int a, int b) {
		return a - b;
	}
	public int mul(int a, int b) {
		return a * b;
	}
	public float div(float a, float b) {
		return a / b;
		
		
	}

}
